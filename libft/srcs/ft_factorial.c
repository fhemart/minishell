#include "libft.h"

int		ft_factorial(int nb)
{
	if (nb == 0)
		return (1);
	if (nb < 0 || nb > 2147483647)
		return (0);
	if (nb == 1)
		return (nb);
	else
	{
		nb = nb * ft_factorial(nb - 1);
		return (nb);
	}
}
