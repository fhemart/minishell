#include "libft.h"

void		ft_putnbr(int n)
{
	long	n2;

	n2 = (long)n;
	if (n2 == -2147483648)
	{
		ft_putstr("-2147483648");
		return ;
	}
	if (n2 < 0)
	{
		ft_putchar('-');
		n2 = n2 * -1;
	}
	if (n2 < 10)
		ft_putchar(n2 + 48);
	else
	{
		ft_putnbr((int)n2 / 10);
		ft_putnbr((int)n2 % 10);
	}
}
